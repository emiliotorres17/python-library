#=========================================================================#
# User defined subroutines                                                #
#=========================================================================#
#-------------------------------------------------------------------------#
# Time derivative                                                         #
#-------------------------------------------------------------------------#
def time_derv(
        field,              # Field
        Nt,                 # number of time steps
        time):              # time variable

    """ Calculating dF/dt using a second order scheme; if it is not at the
    boundary """
    #---------------------------------------------------------------------#
    # Define domain variables                                             #
    #---------------------------------------------------------------------#
    dim         = field.shape
    DT          = np.zeros((dim[0], dim[1], dim[2]))
    delta_t     = time[Nt] - time[Nt-1]
    c1          = 1/( 12.0*delta_t)
    #---------------------------------------------------------------------#
    # Looping over the time and space variables                           #
    #---------------------------------------------------------------------#
    for k in range(0, dim[2]):
        for j in range(0, dim[1]):
            for i in range(0, dim[0]):
                DT[i,j,k]   = c1*(-1.0*field[i,j,k,Nt+2]\
                                    + 8.0*field[i,j,k,Nt+1]\
                                    - 8.0*field[i,j,k,Nt-1]\
                                    + 1.0*field[i,j,k,Nt-2])

    return DT
